package manytomany;

public class villains {
	
	private String name;
	private int powerlvl;
	private String style;
	
	public villains(String name, int powerlvl, String style) {
		this.name = name;
		this.powerlvl = powerlvl;
		this.style = style;
	}

	public String getName() {
		return name;
	}

	public int getPowerlvl() {
		return powerlvl;
	}

	public String getStyle() {
		return style;
	}

	
	

}
